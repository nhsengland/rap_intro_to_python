def add_two_numbers(first_number, second_number):
    """
    Adds two numbers together and returns the result.

    Parameters
    ----------

    first_number (int) : the first number
    second_number (int) : the second number

    Returns
    -------

    int : the sum of the two numbers

    """
    
    sum_of_numbers = first_number + second_number

    return sum_of_numbers

def square_numbers(input_value):
    """
    Returns the square of a numerical input

    Parameters
    ----------
    input_value (int) : numerical input

    Returns
    -------
    
    int : the square of the input value
    
    """

    num_squared = input_value**2
    
    return num_squared